<p class="titlereg">Vegan</p>

<p class="texteplat">Le régime végan est bien plus qu’une simple alimentation, c’est un mode de vie qui exclut toute exploitation animale. 
Les végans ne consomment pas de viande, de poisson, de produits laitiers, d’œufs, de miel ou tout autre produit dérivé des animaux.
 À la place, ils se tournent vers une alimentation exclusivement végétale, privilégiant les fruits, légumes, céréales, légumineuses, 
noix et graines pour répondre à leurs besoins nutritionnels.</p>

<p class="texteplat">Cette démarche est souvent motivée par des convictions éthiques profondes envers le respect 
de tous les êtres vivants, ainsi que par des préoccupations environnementales et de santé. 
En évitant les produits d’origine animale, les végans cherchent à réduire leur empreinte écologique et
 à promouvoir un mode de vie plus durable.</p>

<p class="texteplat">De plus, le mode de vie végan s’étend souvent au-delà de l’alimentation. Les végans évitent 
les produits testés sur les animaux, choisissent des vêtements et des produits cosmétiques fabriqués 
sans matières animales et encouragent des pratiques respectueuses de l’environnement dans tous les aspects de leur vie quotidienne.</p>

