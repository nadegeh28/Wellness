<p class="titlereg">Simple</p>

<p class="texteplat">Un régime simple est un plan alimentaire qui se concentre sur des aliments naturels et non transformés. 
    Il met l’accent sur la consommation d’aliments entiers tels que les fruits, les légumes, 
     les grains entiers, les protéines maigres et les graisses saines 
    comme les noix et les avocats.</p>

<p class="texteplat">Ce régime évite généralement les aliments transformés riches en sucres ajoutés, 
    graisses peu saines et additifs. Plutôt que de se focaliser sur des règles strictes, il privilégie une approche 
    équilibrée des portions et de la nutrition, offrant ainsi plus de flexibilité et de simplicité dans la planification des repas.</p>

<p class="texteplat">L’objectif principal est de favoriser la qualité des aliments plutôt que de se concentrer uniquement 
    sur des restrictions caloriques strictes. Cela rend ce type de régime plus facile à suivre et à intégrer dans la vie quotidienne, 
    favorisant ainsi une alimentation saine et durable sur le long terme.</p>

