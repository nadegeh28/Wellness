<p class="titlereg">MIND</p>

<p class="texteplat">
Le régime MIND, qui signifie « Mediterranean-DASH Intervention for Neurodegenerative Delay » 
(intervention méditerranéenne-DASH pour le retard neurodégénératif), combine les principes du régime 
méditerranéen et du régime DASH (Dietary Approaches to Stop Hypertension). Son objectif principal est de soutenir la santé 
du cerveau et de réduire le risque de déclin cognitif lié à l’âge.

</p>

<p class="texteplat">
Ce régime repose sur des recherches indiquant que certains aliments peuvent avoir des effets positifs sur 
la santé du cerveau, notamment en réduisant l’inflammation et en protégeant les cellules cérébrales. 
Il encourage une alimentation riche en nutriments essentiels, en antioxydants et en graisses saines pour soutenir 
la fonction cérébrale et la santé générale.

</p>

<p class="texteplat">L’approche du régime MIND est souple et adaptable, 
    ce qui permet aux individus de suivre les principes de base tout en personnalisant 
    leurs choix alimentaires en fonction de leurs préférences et de leurs besoins spécifiques.</p>

