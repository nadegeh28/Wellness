<p class="titlereg">Diabétique</p>

<p class="texteplat">
    Le régime pour les personnes diabétiques est conçu pour contrôler la glycémie (le sucre dans le sang). 
    Cela implique de faire attention aux glucides, comme le pain, les pâtes et les sucreries, car ils affectent le niveau de sucre 
    dans le sang. Il est recommandé de privilégier les aliments à faible indice glycémique comme les légumes, les fruits frais et 
    les grains entiers.
</p>

<p class="texteplat">
Il est important de choisir des aliments sains et nutritifs. Cela signifie favoriser les légumes, les fruits, les protéines maigres comme le poulet ou le poisson, et les bonnes graisses provenant des noix, des avocats ou des huiles végétales.

</p>

<p class="texteplat">Garder un poids santé est également crucial pour contrôler le diabète. 
    Chaque personne est différente, donc il est recommandé de travailler avec un professionnel de 
    la santé pour adapter le régime en fonction des besoins individuels. En suivant ce régime et en surveillant 
    de près la glycémie, on peut aider à prévenir les problèmes de santé liés au diabète.</p>

